package Basic;

public class Swap2Number {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("a After value is::"+a);
		System.out.println("a After value is::"+b);
		int teamp=a;
		a=b;
		b=teamp;
		System.out.println("Before value is::"+a);
		System.out.println("Before value is::"+b);
	}

}
